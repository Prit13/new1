import java.util.Scanner;

public class String {
    public static void main(String[] args) {
        Scanner i=new Scanner(System.in);

        System.out.print("Enter 1 word : ");
        String a=i.next();
        System.out.println(a);

        System.out.print("Enter multiple words : ");
        String b=i.nextLine();
        System.out.println(b);

    }
}
